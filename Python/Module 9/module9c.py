# Identity Operators

print("Identity Operators - IS")
a = 2
b = 6
c = a
print(a is b)
print(a is c)

print("---------------------------------------")


print("Identity Operator - IS NOT")
a = 2
b = 6
c = a
print(a is not b)
print(a is not c)

print("---------------------------------------")

#Membership Operators

print("Membership Operators -IN")

numbers = [1,2,3,4]
print(2 in numbers)

colors =['blue','red','yellow']
